#include "libft.h"

char	*ft_strdup(char *str)
{
	int		i;
	char	*ptr;

	ptr = (char *)malloc(ft_strlen(str) * sizeof(char) + 1);
	if (!ptr)
		return (NULL);
	i = 0;
	while (str[i])
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
